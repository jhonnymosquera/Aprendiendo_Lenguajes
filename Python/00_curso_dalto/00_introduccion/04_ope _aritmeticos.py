# suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

# multiplicacion y divicion (* y /)
multiplicacion = 12 * 5
dividido = 12 / 5  # La divicion devuelve un (float)

# Potenciacion (exponentes) (**)
exponente = 12**5

# Divicion baja (//)
# Redondea hacia abajo  ejemplo res = 1.8 ---> 1
divicion_baja = 12//5

# Resto o modulo (%)
resto = 12 % 5


print(f"suma: {suma}")
print(f"resta: {resta}")
print(f"multiplicacion: {multiplicacion}")
print(f"dividido: {dividido}")
print(f"exponente: {exponente}")
print(f"divicion_baja: {divicion_baja}")
print(f"resto: {resto}")

# type(value) muestra el tipo de dato
print(type(exponente))
